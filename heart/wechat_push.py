def get_heartbeat_message():
    """获取本次心跳消息"""
    hour = datetime.now().hour

    # 30%概率使用时段话题，70%使用通用话题
    if random.random() < 0.3:
        if 6 <= hour < 9:
            pool = TIME_TOPICS["morning"]
        elif 11 <= hour < 13:
            pool = TIME_TOPICS["noon"]
        elif 17 <= hour < 20:
            pool = TIME_TOPICS["evening"]
        elif 22 <= hour or hour < 2:
            pool = TIME_TOPICS["night"]
        else:
            pool = TOPIC_POOL
    else:
        pool = TOPIC_POOL

    return random.choice(pool)


if __name__ == "__main__":
    # 独立运行时直接输出消息
    msg = get_heartbeat_message()
    print(msg)
